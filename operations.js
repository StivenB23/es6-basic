export const  addition = (numberFisrt, numbersSecond)=> numberFisrt + numbersSecond;

export const  substract = (numberFisrt, numbersSecond)=> {
  return  numberFisrt - numbersSecond
};

export const  multiplication = (numberFisrt, numbersSecond)=> {
  return  numberFisrt * numbersSecond
};

export const  division = (numberFisrt, numbersSecond)=> {
  return  numberFisrt / numbersSecond
};

export const  potence = (base, number) => {
  return  Math.pow(base, number);
};
